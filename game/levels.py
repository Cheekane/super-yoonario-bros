def level_rows(idx):
    return LEVELS[idx]["build"]()


def validate():
    """Sanity-check every level; returns list of problem strings."""
    problems = []
    for i, lv in enumerate(LEVELS):
        rows = lv["build"]()
        widths = {len(r) for r in rows}
        if len(widths) != 1:
            problems.append(f"{lv['name']}: ragged rows {widths}")
        if len(rows) != ROWS:
            problems.append(f"{lv['name']}: {len(rows)} rows")
        joined = "".join(rows)
        if "P" not in joined:
            problems.append(f"{lv['name']}: no player spawn")
        if lv["boss_hp"] == 0 and "F" not in joined:
            problems.append(f"{lv['name']}: no flag")
        if lv["boss_hp"] > 0 and "*" not in joined:
            problems.append(f"{lv['name']}: no boss")
    return problems
