"""Level data for all worlds, written as ASCII tile maps.

Legend:
  .  air                       X  ground/dirt block
  B  brick (breakable if big)  =  solid block
  ?  ?-block with coin         M  ?-block with power-up
  U  ?-block with 1-up         -  one-way platform (jump through)
  [ ]  pipe top                { }  pipe body
  ( )  pipe top w/ Chomp Plant ^  spikes
  L  lava                      b  bridge (solid)
  C  coin                      A  checkpoint
  P  player spawn              F  flag (pole auto-extends down)
  E  Grub                      K  Shellback
  S  Spiny                     W  Flit (flying)
  o  moving platform (horiz)   v  moving platform (vert)
  *  boss

Levels are 15 rows tall. Chunks are bottom-aligned then concatenated.
"""

ROWS = 15


def blk(s):
    """Parse a chunk: strip empty edge lines, pad rows to equal width,
    bottom-align to ROWS rows of air."""
    lines = [ln for ln in s.splitlines()]
    while lines and not lines[0].strip():
        lines.pop(0)
    while lines and not lines[-1].strip():
        lines.pop()
    w = max(len(ln) for ln in lines) if lines else 0
    lines = [ln.ljust(w, ".") for ln in lines]
    pad = ["." * w] * (ROWS - len(lines))
    return pad + lines


def flat(w, h=2):
    return blk("\n".join(["X" * w] * h))


def gap(w):
    return blk("." * w)


def hcat(*chunks):
    return ["".join(rows) for rows in zip(*chunks)]


