_cache = {}


def glyph(ch, scale, color):
    ch = ch.upper()
    if ch not in _G:
        ch = "?"
    key = (ch, scale, color)
    surf = _cache.get(key)
    if surf is None:
        surf = pygame.Surface((W * scale, H * scale), pygame.SRCALPHA)
        for gy, row in enumerate(_G[ch]):
            for gx, c in enumerate(row):
                if c == "#":
                    surf.fill(color, (gx * scale, gy * scale, scale, scale))
        _cache[key] = surf
    return surf


def measure(s, scale=1):
    return (len(s) * (W + 1) - 1) * scale if s else 0


def draw(surf, s, x, y, color, scale=1, center=False, shadow=True):
    """Draw text; returns its bounding rect."""
    w = measure(s, scale)
    if center:
        x -= w // 2
    if shadow:
        for i, ch in enumerate(s):
            if ch != " ":
                surf.blit(glyph(ch, scale, (10, 10, 10)),
                          (x + i * (W + 1) * scale + scale, y + scale))
    for i, ch in enumerate(s):
        if ch != " ":
            surf.blit(glyph(ch, scale, color), (x + i * (W + 1) * scale, y))
    return pygame.Rect(x, y, w, H * scale)
